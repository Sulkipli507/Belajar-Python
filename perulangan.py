#perulangan while 
i = 1
while i <= 5:
    print(i)
    i += 1
print("\n") 
   
angka = 0
while (angka < 9):
    print("tampil angka", angka)
    angka = angka + 1
print("Good bye!")
print("\n") 

#perulangan for
for i in range(5):
    print("Perulangan ke -", i)
print("\n") 
    
angka = [1,2,3,4,5]
for x in angka:
    print(x)
print("\n") 
   
